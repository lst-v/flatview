"""Self-contained HTML report with interactive plotly charts.

Plotly is loaded from the CDN (`include_plotlyjs="cdn"`) — the file is small
and opens in any browser with internet. Switch to `include_plotlyjs=True` if
you need a fully offline bundle (~3MB heavier).
"""

